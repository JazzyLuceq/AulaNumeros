lista = [28, 9, 22, -31, -3, -31, 10, 32, 10, 2]

# Usando while para imprimir todos os itens da lista
indice = 0
while indice < len(lista):
    print(lista[indice])
    indice += 1

# Comparação com o exercício 1 (usando for loop)
print("\nUsando for loop:")
for elemento in lista:
    print(elemento)
